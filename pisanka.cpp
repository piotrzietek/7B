#include <bits/stdc++.h>
#include <windows.h>

using namespace std;

int main()
{
    for (int i = 0; i < 20; i++) {
        cout<<"         *  "<<endl;
        cout<<"        *#*  "<<endl;
        cout<<"       **#** "<<endl;
        cout<<"      *#*#*#*"<<endl;
        cout<<"       **#** "<<endl;
        cout<<"        *#*  "<<endl;
        cout<<"         *   "<<endl;

        Sleep(200);
        system("cls");

        cout<<"         *  "<<endl;
        cout<<"        *&*  "<<endl;
        cout<<"       **&** "<<endl;
        cout<<"      *&*&*&*"<<endl;
        cout<<"       **&** "<<endl;
        cout<<"        *&*  "<<endl;
        cout<<"         *   "<<endl;
        Sleep(200);
        system("cls");
    }
    return 0;
}
